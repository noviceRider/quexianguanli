package com.huibo.issue.bo;

import java.util.Arrays;

import com.huibo.issue.po.UserPo;

public class UserBo extends UserPo {
	
	/**
	 * 
	 */
	private String pId ;
	
	/**
	 * 
	 */
	private String roleCode ;
	
	/**
	 * 
	 * @return
	 */
	
	private String userIds ;
	
	/**
	 * 
	 */
	private String[]  usIds ;

	public String getpId() {
		return pId;
	}

	public void setpId(String pId) {
		this.pId = pId;
	}

	public String getRoleCode() {
		return roleCode;
	}

	public void setRoleCode(String roleCode) {
		this.roleCode = roleCode;
	}

	public String getUserIds() {
		return userIds;
	}

	public void setUserIds(String userIds) {
		this.userIds = userIds;
	}

	public String[] getUsIds() {
		return usIds;
	}

	public void setUsIds(String[] usIds) {
		this.usIds = usIds;
	}

	@Override
	public String toString() {
		return "UserBo [pId=" + pId + ", roleCode=" + roleCode + ", userIds=" + userIds + ", usIds="
				+ Arrays.toString(usIds) + "]";
	}

	
}
