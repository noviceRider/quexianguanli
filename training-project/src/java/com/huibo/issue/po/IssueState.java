public class IssueState extends BasePo {

	/**
	 * 状态编号
	 */
	private String stateCode;
	
	/**
	 * 状态描述
	 */
	private String stateDesc;
	
	/**
	 * 当前状态
	 */
	private String moduleState;
	
	/**
	 * 创建人
	 */
	private String createBy;
	
	/**
	 * 创建时间
	 */
	private String createTime;
	
	/**
	 * 修改人
	 */
	private String modifyBy;
	
	/**
	 * 修改时间
	 */
	private String modifyTime;

	public IssueState() {
		super();
		// TODO Auto-generated constructor stub
	}

	public IssueState(String stateCode, String stateDesc, String moduleState, String createBy, String createTime,
			String modifyBy, String modifyTime) {
		super();
		this.stateCode = stateCode;
		this.stateDesc = stateDesc;
		this.moduleState = moduleState;
		this.createBy = createBy;
		this.createTime = createTime;
		this.modifyBy = modifyBy;
		this.modifyTime = modifyTime;
	}

	@Override
	public String toString() {
		return "iIssueState [stateCode=" + stateCode + ", stateDesc=" + stateDesc + ", moduleState=" + moduleState
				+ ", createBy=" + createBy + ", createTime=" + createTime + ", modifyBy=" + modifyBy + ", modifyTime="
				+ modifyTime + "]";
	}

	/**
	 * 存取器
	 * @return
	 */
	public String getStateCode() {
		return stateCode;
	}

	public void setStateCode(String stateCode) {
		this.stateCode = stateCode;
	}

	public String getStateDesc() {
		return stateDesc;
	}

	public void setStateDesc(String stateDesc) {
		this.stateDesc = stateDesc;
	}

	public String getModuleState() {
		return moduleState;
	}

	public void setModuleState(String moduleState) {
		this.moduleState = moduleState;
	}

	public String getCreateBy() {
		return createBy;
	}

	public void setCreateBy(String createBy) {
		this.createBy = createBy;
	}

	public String getCreateTime() {
		return createTime;
	}

	public void setCreateTime(String createTime) {
		this.createTime = createTime;
	}

	public String getModifyBy() {
		return modifyBy;
	}

	public void setModifyBy(String modifyBy) {
		this.modifyBy = modifyBy;
	}

	public String getModifyTime() {
		return modifyTime;
	}

	public void setModifyTime(String modifyTime) {
		this.modifyTime = modifyTime;
	}

}
