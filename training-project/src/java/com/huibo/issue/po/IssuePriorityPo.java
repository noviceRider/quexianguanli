public class IssuePriorityPo extends BasePo {
	/**
	 * 紧急程度编号
	 */
	private String priorityCode;
	
	/**
	 * 紧急程度描述
	 */
	private String priorityDesc;
	
	/**
	 * 当前状态
	 */
	private String priorityState;
	
	/**
	 * 创建人
	 */
	private String createBy;
	
	/**
	 * 创建时间
	 */
	
	private String createTime;
	
	/**
	 * 修改人
	 */
	private String modifyBy;
	
	/**
	 * 修改时间
	 */
	
	private String modifyTime;

	public IssuePriorityPo() {
		super();
		// TODO Auto-generated constructor stub
	}

	public IssuePriorityPo(String priorityCode, String priorityDesc, String priorityState, String createBy,
			String createTime, String modifyBy, String modifyTime) {
		super();
		this.priorityCode = priorityCode;
		this.priorityDesc = priorityDesc;
		this.priorityState = priorityState;
		this.createBy = createBy;
		this.createTime = createTime;
		this.modifyBy = modifyBy;
		this.modifyTime = modifyTime;
	}

	@Override
	public String toString() {
		return "IssuePriorityPo [priorityCode=" + priorityCode + ", priorityDesc=" + priorityDesc + ", priorityState="
				+ priorityState + ", createBy=" + createBy + ", createTime=" + createTime + ", modifyBy=" + modifyBy
				+ ", modifyTime=" + modifyTime + "]";
	}

	public String getPriorityCode() {
		return priorityCode;
	}

	public void setPriorityCode(String priorityCode) {
		this.priorityCode = priorityCode;
	}

	public String getPriorityDesc() {
		return priorityDesc;
	}

	public void setPriorityDesc(String priorityDesc) {
		this.priorityDesc = priorityDesc;
	}

	public String getPriorityState() {
		return priorityState;
	}

	public void setPriorityState(String priorityState) {
		this.priorityState = priorityState;
	}

	public String getCreateBy() {
		return createBy;
	}

	public void setCreateBy(String createBy) {
		this.createBy = createBy;
	}

	

	public String getModifyBy() {
		return modifyBy;
	}

	public void setModifyBy(String modifyBy) {
		this.modifyBy = modifyBy;
	}

	public String getCreateTime() {
		return createTime;
	}

	public void setCreateTime(String createTime) {
		this.createTime = createTime;
	}

	public String getModifyTime() {
		return modifyTime;
	}

	public void setModifyTime(String modifyTime) {
		this.modifyTime = modifyTime;
	}

	
	
	
	
}
